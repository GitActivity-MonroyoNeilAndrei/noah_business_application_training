NSD_ShowToolbox();

function NSD_ShowToolbox() {
    var len = 0;
    try { len = _NOAHSpread_MenuTab.length; } catch (ex) { }
    for (var i = 0; i < len; i++) {
        var item = _NOAHSpread_MenuTab[i];
        var id = p8Spread_GetJsonValue(item, "id");
        var show = p8Spread_GetJsonValue(item, "show");
        var status = p8Spread_GetJsonValue(item, "status");
        if (show) {
            $("#" + id).removeClass("tbhide");
        } else {
            $("#" + id).addClass("tbhide");
        }
        if (!p8Spread_IsNull(status)) {
            $("#" + id).addClass(status);
            //$("#" + id).css("background-color", status);
        } 
    }
}


